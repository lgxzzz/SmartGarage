    public void initData(){
        CarPort carPort = (CarPort) getIntent().getExtras().getSerializable("carPort");
        boolean isFast = getIntent().getExtras().getBoolean("isFast");
        if (isFast){
            mTitleView.setTitle("导航路线");
        }else{
            mTitleView.setTitle("路线回放");
        }
        if (carPort!=null){
            LatLng startLng = DBManger.getInstance(this).getmCurPoint();
            mStartPoint = new LatLonPoint(startLng.latitude,startLng.longitude);
            mEndPoint = new LatLonPoint(carPort.getLat(),carPort.getLon());
            calculateDriveRoute();
        }
    };

    public void calculateDriveRoute(){
        /**
         * 方法:
         *   int strategy=mAMapNavi.strategyConvert(congestion, avoidhightspeed, cost, hightspeed, multipleroute);
         * 参数:
         * @congestion 躲避拥堵
         * @avoidhightspeed 不走高速
         * @cost 避免收费
         * @hightspeed 高速优先
         * @multipleroute 多路径
         *
         * 说明:
         *      以上参数都是boolean类型，其中multipleroute参数表示是否多条路线，如果为true则此策略会算出多条路线。
         * 注意:
         *      不走高速与高速优先不能同时为true
         *      高速优先与避免收费不能同时为true
         */
        int strategy=0;
        try {
            strategy = mAMapNavi.strategyConvert(true, false, false, false, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
        List<NaviLatLng> startList = new ArrayList<NaviLatLng>();
        /**
         * 途径点坐标集合
         */
        List<NaviLatLng> wayList = new ArrayList<NaviLatLng>();
        /**
         * 终点坐标集合［建议就一个终点］
         */
        List<NaviLatLng> endList = new ArrayList<NaviLatLng>();

        NaviLatLng startPoi = new NaviLatLng(mStartPoint.getLatitude(), mStartPoint.getLongitude());
        NaviLatLng endPoi = new NaviLatLng(mStartPoint.getLatitude(), mStartPoint.getLongitude());

        startList.add(startPoi);
        endList.add(endPoi);

        mAMapNavi.calculateDriveRoute(startList, endList, wayList, strategy);
    }

